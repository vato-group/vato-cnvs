const DICTS: Record<Lang, Dict> = { en, fr, nl };

/** Resolve a key for a given language, falling back to French, then the key. */
function translate(lang: Lang, key: string, vars?: Vars): string {
  const entry = DICTS[lang]?.[key] ?? fr[key];
  if (entry === undefined) return key;
  return typeof entry === "function" ? entry(vars ?? {}) : interpolate(entry, vars);
}

/** Standalone translator bound to the current store language (non-React code). */
export function gt(key: string, vars?: Vars): string {
  return translate(useStore.getState().settings.lang, key, vars);
}

/** The current app language (non-React read). */
export const getLang = (): Lang => useStore.getState().settings.lang;

export type TFn = (key: string, vars?: Vars) => string;

/** React hook: a translator bound to the live language (re-renders on change). */
export function useT(): TFn {
  const lang = useStore((s) => s.settings.lang);
  return useMemo<TFn>(() => (key, vars) => translate(lang, key, vars), [lang]);
}

/** React hook: the live app language. */
export const useLang = (): Lang => useStore((s) => s.settings.lang);
